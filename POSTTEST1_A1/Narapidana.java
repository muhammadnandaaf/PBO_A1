class Narapidana {
    String nama;
    String asal;
    String alasan;
    int usia;
    double waktu;

    public Narapidana(String nama, String asal, String alasan, int usia,double waktu){
        this.nama = nama;
        this.asal = asal;
        this.alasan = alasan;
        this.usia = usia;
        this.waktu = waktu;
    }
}
